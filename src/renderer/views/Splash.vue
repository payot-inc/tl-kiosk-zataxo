<template>
  <div class="container main">
    <div class="inner">
      <div class="visual_sb">
        <swiper
          :loop="true"
          page-transition="fade"
          :interval="5000"
          :autoplay="true"
          :show-indicator="false"
        >
          <swiper-item v-for="path in images" :key="path">
            <img :src="path" />
          </swiper-item>
        </swiper>
      </div>

      <div class="start">
        <router-link :to="{ name: 'menu' }" class="ui button">시작하기</router-link>
      </div>
      <div class="logo" @click="logout">
        <img src="@/assets/image/the_logo.png" style="opacity: 0;" />
      </div>

      <div class="wave-box">
        <div class="wave -one"></div>
        <div class="wave -two"></div>
        <div class="wave -three"></div>
      </div>
    </div>
  </div>
</template>

<script>
import { Swiper, SwiperItem } from 'vue-h5-swiper';
import { mapState, mapMutations } from 'vuex';

export default {
  name: 'Splash',
  components: {
    Swiper,
    SwiperItem,
  },
  data() {
    return {
      images: [
        'http://admin.payot-coin.com/kiosk/zatacso/sb01.png',
      ],
      clickCounter: 0,
    };
  },
  computed: {
    ...mapState('kiosk', ['company', 'options']),
  },
  methods: {
    ...mapMutations('kiosk', { setCompany: 'SET_COMPANY' }),
    logout() {
      if (this.clickCounter !== 15) {
        this.clickCounter += 1;
        return;
      }

      const { email } = this.company;
      this.setCompany({ email });
      this.$router.push({ name: 'login' });
    },
  },
};
</script>